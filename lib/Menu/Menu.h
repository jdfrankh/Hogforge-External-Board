class Tnode {
public:
    String data;
    std::vector<Tnode*> children;
    Tnode* back;
    int storeLevel;
    int act; // Function pointer for action

    std::vector<String> getChildrenNames() {
        std::vector<String> names;
        for (Tnode* child : children) {
            names.push_back(child->data);
        }
        return names;
    }

   
    Tnode(String name, int fn, int a = 0) {
        data = name;
        back = nullptr;
        storeLevel = fn;
        act = a; // Function pointer for action
        //left = right = nullptr;
    
    }
};

